package threakTask;

public class Grade {
	protected String grade_name;
	protected double mark;
	
	
	public Grade(){
		
	}


	public String getGrade_name() {
		return grade_name;
	}


	public void setGrade_name(String grade_name) {
		this.grade_name = grade_name;
	}


	public double getMark() {
		return mark;
	}


	public void setMark(double mark) {
		this.mark = mark;
	}
	
	
	

}
